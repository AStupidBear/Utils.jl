module Units

using Utils

@export_const Float begin
  metre = 1e2
  meter = metre
  cm = metre / 1e2
  mm = metre / 1e3
  um = metre / 1e6
  nm = metre / 1e9

  second = 1e3
  ms = second / 1e3
  Hz = 1 / second

  voltage = 1e3
  mV = voltage / 1e3

  ampere = 1e6
  mA = ampere / 1e3
  uA = ampere / 1e6
  nA = ampere / 1e9

  farad=1e6
  uF = farad/1e6
  ufarad = uF

  siemens = 1e3
  mS = siemens / 1e3
  msiemens = mS
  nS = siemens / 1e9
  nsiemens = nS
end

end
